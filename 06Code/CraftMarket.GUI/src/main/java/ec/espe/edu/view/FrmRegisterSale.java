    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed
        // TODO add your handling code here:
        String productId = txtProductId.getText().trim();
        int quantity = (int) spnQuantity.getValue();

        if (productId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favoringrese el ID del producto.");
            return;
        }

        Product product = Product.findById(productId);

        if (product == null) {
            JOptionPane.showMessageDialog(this, "Producto no encontrado.");
            return;
        }

        if (quantity > product.getStock()) {
            JOptionPane.showMessageDialog(this, "Stock insuficiente. Solo hay " + product.getStock() + " unidades disponibles.");
            return;
        }

        float unitPrice = product.getUnitPrice();
        float total = unitPrice * quantity;
        String artisanName = product.getOwner();

        SalesReport sale = new SalesReport(product.getName(), unitPrice, quantity, total, artisanName);
        SalesReport.registerSale(sale);

        int newStock = product.getStock() - quantity;
        Product.updateProductStock(product.getId(), newStock);

        JOptionPane.showMessageDialog(this, "Venta registrada exitosamente.\nTotal: $" + total);
        txtProductId.setText("");
        spnQuantity.setValue(1);
        txtProductId.requestFocus();
    }//GEN-LAST:event_btnSaleActionPerformed

