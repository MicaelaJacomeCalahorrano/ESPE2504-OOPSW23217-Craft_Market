    private void loadProductsToTable() {
        List<Product> products = Product.getAllProducts();

        DefaultTableModel model = (DefaultTableModel) tblProducts.getModel();
        model.setRowCount(0);

        for (Product product : products) {
            Object[] row = {
                product.getId(),
                product.getName(),
                product.getUnitPrice(),
                product.getStock(),
                product.getOwner()
            };
            model.addRow(row);
        }
    }

