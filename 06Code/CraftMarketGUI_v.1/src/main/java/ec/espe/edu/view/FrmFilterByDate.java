    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
         String dateStr = txtDate.getText().trim();

    try {
      
        LocalDate date = LocalDate.parse(dateStr);

        MongoCollection<Document> collection = Connection.getDatabase().getCollection("sales");

       
        Document filter = new Document("saleDate", date.toString());
        MongoCursor<Document> cursor = collection.find(filter).iterator();

        
        DefaultTableModel model = (DefaultTableModel) tblSales.getModel();
        model.setRowCount(0); 

        try {
            while (cursor.hasNext()) {
                Document doc = cursor.next();

                String productName = doc.getString("productName");
                double unitPrice = doc.getDouble("unitPrice");
                int quantity = doc.getInteger("quantity");
                double total = doc.getDouble("total");
                String artisanName = doc.getString("artisanName");

                Object[] row = {
                    productName,
                    unitPrice,
                    quantity,
                    total,
                    artisanName
                };

                model.addRow(row);
            }
        } finally {
            cursor.close();
        }

        if (model.getRowCount() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "No se encontraron ventas en la fecha especificada.");
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: Aseg√∫rate de ingresar la fecha en formato yyyy-MM-dd.\n" + e.getMessage());
    }
    }//GEN-LAST:event_btnSearchActionPerformed

